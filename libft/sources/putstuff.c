#include "ft_printf.h"

void	putflg(t_gen **a, wchar_t c)
{
	if (c == '#')
		((*a)->flg)->hsh += 1;
	if (c == '0')
		((*a)->flg)->zr += 1;
	if (c == '-')
		((*a)->flg)->min += 1;
	if (c == ' ')
		((*a)->flg)->sp += 1;
	if (c == '+')
		((*a)->flg)->plu += 1;
	if (c == 39)
		((*a)->flg)->apos += 1;
	((*a)->flg)->sum += 1;
}

void	putmod(t_gen **a, wchar_t c)
{
	if (c == 'h')
		((*a)->md)->h += 1;
	if (c == 'l')
		((*a)->md)->l += 1;
	if (c == 'j')
		((*a)->md)->j += 1;
	if (c == 'L')
		((*a)->md)->ll += 1;
	if (c == 'z')
		((*a)->md)->z += 1;
	((*a)->md)->sum += 1;
}
